print(qc.depth())
transpile_circuit = transpile(qc, Aer.get_backend(selected_backend), basis_gates = ['cx', 'id', 'rz', 'sx', 'x'])
dag_circuit = circuit_to_dag(transpile_circuit)
print(dag_circuit.count_ops_longest_path())
